    '3343315615-3343342551',
    '8388258268-8388317065',
    '632952-689504',
    '3-22',
    '988344-1007943',
  ];

  int totalSum = 0;

  for (String range in ranges) {
    var parts = range.split('-');
    int start = int.parse(parts[0]);
    int end = int.parse(parts[1]);

    for (int i = start; i <= end; i++) {
      String s = i.toString();
      if (s.length % 2 == 0) {
        int mid = s.length ~/ 2;
        if (s.substring(0, mid) == s.substring(mid)) {
          totalSum += i;
        }
      }
    }
  }

  print(totalSum);
}
